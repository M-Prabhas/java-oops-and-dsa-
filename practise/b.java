package practise;

import oops.inheritance;

public class b extends inheritance {
	public b(String name) {
		super(name);
		System.out.println("sing constructor");
	}
	public void sing() {
		System.out.println(name+"  singer sings");
	}
	public void eat() {
		System.out.println(name+"   singer is eating");
	}
	public static void laugh() {
		System.out.println(" singer is laughing");
	}}
	

